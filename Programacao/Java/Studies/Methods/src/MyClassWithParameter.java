public class MyClassWithParameter {
    public static void myMethodParameter(String fname){
        System.out.println(fname + " Cristina");
    }

    public static void myMethodMultiParameter(String fname, int age){
        System.out.println(fname + " with age : " + age);
    }
    public static int myMethodReturnValues(int number){
        return (5 + number);
    }

    public static float myMethodReturnValues(float number){
        return (5.5f + number);
    }
}
